function min(num1, num2) {
  // use if else statements to determine which number is smaller
if (num2 === num1){
  // return num1 if equal
  return num1;
}
else if (num1 < num2){
  // return num1 if num1 is less than num2
  return num1;
}
else if (num1 > num2){
  // return num2 if num1 is greater than num2
  return num2;
}
}

